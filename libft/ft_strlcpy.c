#include "libft.h"

size_t	ft_strlcpy(char *dst, char *src, unsigned int size)
{
	unsigned int	i;

	i = 0;
	if (!src || !dst)
		return (NULL);
	if (size == 0)
		return (ft_strlen(src));
	while (i < size - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ft_strlen(src));
}
